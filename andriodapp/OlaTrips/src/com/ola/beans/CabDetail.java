package com.ola.beans;


public class CabDetail {
	
	private String cabId;
	private String distanceTravelled;
	private String cabType;
	private String cabLowEstimate;
	private String cabHighEstimate;
	private String cabActualEstimate;
	private String cabDuration;
	private String cabDistance;
	private String capacity;
	private String cabActualCost;
	
	public String getCapacity() {
		return capacity;
	}
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}
	public String getCabActualCost() {
		return cabActualCost;
	}
	public void setCabActualCost(String cabActualCost) {
		this.cabActualCost = cabActualCost;
	}
	public String getCabId() {
		return cabId;
	}
	public void setCabId(String cabId) {
		this.cabId = cabId;
	}
	public String getDistanceTravelled() {
		return distanceTravelled;
	}
	public void setDistanceTravelled(String distanceTravelled) {
		this.distanceTravelled = distanceTravelled;
	}
	public String getCabType() {
		return cabType;
	}
	public void setCabType(String cabType) {
		this.cabType = cabType;
	}
	public String getCabLowEstimate() {
		return cabLowEstimate;
	}
	public void setCabLowEstimate(String cabLowEstimate) {
		this.cabLowEstimate = cabLowEstimate;
	}
	public String getCabHighEstimate() {
		return cabHighEstimate;
	}
	public void setCabHighEstimate(String cabHighEstimate) {
		this.cabHighEstimate = cabHighEstimate;
	}
	public String getCabActualEstimate() {
		return cabActualEstimate;
	}
	public void setCabActualEstimate(String cabActualEstimate) {
		this.cabActualEstimate = cabActualEstimate;
	}
	public String getCabDuration() {
		return cabDuration;
	}
	public void setCabDuration(String cabDuration) {
		this.cabDuration = cabDuration;
	}
	public String getCabDistance() {
		return cabDistance;
	}
	public void setCabDistance(String cabDistance) {
		this.cabDistance = cabDistance;
	}
	
	

}
